struct hdmi_v_params
{
	u16 h_blank;          	/* H Blank */
	u32 v_blank;          	/* V Blank */
	u32 hvline;          	/* HVLine */
	u32 h_sync_gen;        	/* HSYNCGen */
	u32 v_sync_gen;		/* VSYCGen don't care the frequency (50/60) */
	u8  avi_vic;		/* CEA VIC */
	u8  avi_vic_16_9;	/* CEA VIC */
	u8  interlaced;		/* 0 - non-interlaced, 1 - interlaced */
	u8  repetition;		/* pixel repetition */
	u8  polarity;		/* VSYNC polarity */
	u32 v_blank_f;		/* VBLANK for Interlaced */
	u32 v_sync_gen2;	/* VSYNC for Interlaced */
	u32 v_sync_gen3;
	phy_freq pixel_clock;	/* pixel clock */
};

/* HDMI Timing generation register setting */
struct _hdmi_tg_param
{
	u16 h_fsz;
	u16 hact_st;
	u16 hact_sz;
	u16 v_fsz;
	u16 vsync;
	u16 vsync2;
	u16 vact_st;
	u16 vact_sz;
	u16 field_chg;
	u16 vact_st2;
	u16 vsync_top_hdmi;
	u16 vsync_bot_hdmi;
	u16 field_top_hdmi;
	u16 field_bot_hdmi;
	u8 mhl_hsync_width;
	u8 mhl_vsync_width;
};

